run_check() {
    local name="$1"
    local required="$2"
    local notes="$3"
    shift 3

    local log_file="$LOG_DIR/${name}.log"

    echo "==> ${name} (${required})"
    if (
        cd "$ROOT_DIR"
        "$@"
    ) >"$log_file" 2>&1; then
        echo "    ok (${log_file})"
        echo "${name},${required},pass,\"${notes}\",${log_file}" >>"$SUMMARY_CSV"
        return 0
    fi

    echo "    fail (${log_file})"
    echo "${name},${required},fail,\"${notes}\",${log_file}" >>"$SUMMARY_CSV"
    all_failures=$((all_failures + 1))
    if [[ "$required" == "required" ]]; then
        required_failures=$((required_failures + 1))
    fi
    return 0
}

summary_value_from_log() {
    local key="$1"
    local log_file="$2"
    if [[ ! -f "$log_file" ]]; then
        return 0
    fi
    sed -nE "s/^${key}: (.*)$/\\1/p" "$log_file" | tail -n 1 || true
}

