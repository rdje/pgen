package AST::BacktrackingParserGenerator;

use strict;
use warnings;
use Data::Dumper;
use AST::PackratParser;

=head1 NAME

AST::BacktrackingParserGenerator - Generate packrat parsers with infinite backtracking

=head1 DESCRIPTION

This module generates parser code that uses the AST::PackratParser runtime
to provide true infinite backtracking with complete memoization. 

Key features:
- Handles all quantifier types including grouped quantifiers
- Full return annotation system integration
- Left recursion support via existing AST::LeftRecursion
- HDL-optimized parser generation
- Fixes the SKIPPED grouped quantifier bug

=cut

=head2 generate_packrat_parser($grammar_rules, $main_rule, $options)

Generate a complete packrat parser from grammar rules.

=cut

sub generate_packrat_parser {
    my ($grammar_rules, $main_rule, $options) = @_;
    $options ||= {};
    
    my $parser_package = $options->{package} || 'GeneratedPackratParser';
    my $verbose = $options->{verbose} || 0;
    
    # Generate parser header
    my $parser_code = generate_parser_header($parser_package);
    
    # Generate main parse function
    $parser_code .= generate_main_parse_function($main_rule);
    
    # Generate individual rule parsers
    for my $rule_name (keys %$grammar_rules) {
        my $rule_def = $grammar_rules->{$rule_name};
        $parser_code .= generate_rule_parser($rule_name, $rule_def, $options);
    }
    
    # Generate parser footer
    $parser_code .= generate_parser_footer();
    
    return $parser_code;
}

=head2 generate_parser_header($package_name)

Generate the parser header with necessary imports and setup.

=cut

sub generate_parser_header {
    my ($package_name) = @_;
    
    return <<EOF;
package $package_name;

use strict;
use warnings;
use AST::PackratParser;

# =============================================================================
# GENERATED PACKRAT PARSER WITH INFINITE BACKTRACKING AND MEMOIZATION
# Generated by AST::BacktrackingParserGenerator
# =============================================================================

EOF
}

=head2 generate_main_parse_function($main_rule)

Generate the main parser entry point.

=cut

sub generate_main_parse_function {
    my ($main_rule) = @_;
    
    return <<EOF;
# Main parsing entry point
sub parse {
    my (\$input_ref) = \@_;
    
    # Clear memoization cache for fresh parse
    AST::PackratParser::clear_memo_cache();
    
    # Start parsing from position 0
    pos(\$\$input_ref) = 0;
    
    # Parse using the main rule
    return parse_$main_rule(\$input_ref, 0);
}

# Get parsing statistics  
sub get_parse_stats {
    return AST::PackratParser::get_parse_stats();
}

# Get cache efficiency information
sub get_cache_efficiency {
    return AST::PackratParser::get_cache_efficiency();
}

EOF
}

=head2 generate_rule_parser($rule_name, $rule_def, $options)

Generate parser function for a single grammar rule.

=cut

sub generate_rule_parser {
    my ($rule_name, $rule_def, $options) = @_;
    
    my $verbose = $options->{verbose} || 0;
    
    if ($verbose) {
        print STDERR "Generating parser for rule: $rule_name\n";
        print STDERR "Rule definition: " . Dumper($rule_def) . "\n";
    }
    
    # Determine rule type based on structure
    if (has_alternatives($rule_def)) {
        return generate_alternatives_parser($rule_name, $rule_def, $options);
    } elsif (has_sequence($rule_def)) {
        return generate_sequence_parser($rule_name, $rule_def, $options);
    } elsif (has_quantifier($rule_def)) {
        return generate_quantified_parser($rule_name, $rule_def, $options);
    } else {
        return generate_atom_parser($rule_name, $rule_def, $options);
    }
}

=head2 generate_alternatives_parser($rule_name, $rule_def, $options)

Generate parser for rule with alternatives (OR groups).

=cut

sub generate_alternatives_parser {
    my ($rule_name, $rule_def, $options) = @_;
    
    my @alternatives = @{$rule_def->{or_groups}};
    my $return_annotation = $rule_def->{return_annotation};
    
    my $code = "sub parse_$rule_name {\n";
    $code .= "    my (\$input_ref, \$pos) = \@_;\n";
    $code .= "    \$pos = pos(\$\$input_ref) unless defined \$pos;\n";
    $code .= "    \n";
    $code .= "    return AST::PackratParser::memoized_rule('$rule_name', \$input_ref, \$pos, sub {\n";
    $code .= "        my (\$input_ref, \$pos) = \@_;\n";
    $code .= "        return AST::PackratParser::try_alternatives(\$input_ref, \$pos,\n";
    
    # Generate each alternative
    for my $i (0..$#alternatives) {
        my $alt = $alternatives[$i];
        my $alt_code = generate_alternative_code($alt, $rule_name, $i, $options);
        $code .= "            sub { $alt_code },\n";
    }
    
    $code .= "        );\n";
    $code .= "    });\n";
    $code .= "}\n\n";
    
    return $code;
}

=head2 generate_sequence_parser($rule_name, $rule_def, $options)

Generate parser for rule with sequence of elements.

=cut

sub generate_sequence_parser {
    my ($rule_name, $rule_def, $options) = @_;
    
    my @elements = @{$rule_def->{elements}};
    my $return_annotation = $rule_def->{return_annotation};
    
    my $code = "sub parse_$rule_name {\n";
    $code .= "    my (\$input_ref, \$pos) = \@_;\n";
    $code .= "    \$pos = pos(\$\$input_ref) unless defined \$pos;\n";
    $code .= "    \n";
    $code .= "    return AST::PackratParser::memoized_rule('$rule_name', \$input_ref, \$pos, sub {\n";
    $code .= "        my (\$input_ref, \$pos) = \@_;\n";
    $code .= "        return AST::PackratParser::parse_sequence(\$input_ref, \$pos,\n";
    
    # Generate each sequence element
    for my $element (@elements) {
        my $element_code = generate_element_code($element, $rule_name, $options);
        $code .= "            sub { $element_code },\n";
    }
    
    $code .= "        );\n";
    $code .= "    });\n";
    $code .= "}\n\n";
    
    return $code;
}

=head2 generate_quantified_parser($rule_name, $rule_def, $options)  

Generate parser for quantified rule elements.

=cut

sub generate_quantified_parser {
    my ($rule_name, $rule_def, $options) = @_;
    
    my $element = $rule_def->{element};
    my $quantifier = $rule_def->{quantifier};
    my ($min, $max) = parse_quantifier_bounds($quantifier);
    
    my $code = "sub parse_$rule_name {\n";
    $code .= "    my (\$input_ref, \$pos) = \@_;\n";
    $code .= "    \$pos = pos(\$\$input_ref) unless defined \$pos;\n";
    $code .= "    \n";
    $code .= "    return AST::PackratParser::memoized_rule('$rule_name', \$input_ref, \$pos, sub {\n";
    $code .= "        my (\$input_ref, \$pos) = \@_;\n";
    
    if (is_grouped_quantifier($element)) {
        # Handle grouped quantifiers like (',' /\s*/ return_expression)*
        my @group_elements = @{$element->{group_elements}};
        
        $code .= "        my \@group_parsers = (\n";
        for my $group_element (@group_elements) {
            my $group_code = generate_element_code($group_element, $rule_name, $options);
            $code .= "            sub { $group_code },\n";
        }
        $code .= "        );\n";
        $code .= "        return AST::PackratParser::parse_grouped_quantified(\$input_ref, \$pos, \\\@group_parsers, $min, $max);\n";
    } else {
        # Regular quantified element
        my $element_code = generate_element_code($element, $rule_name, $options);
        $code .= "        my \$element_parser = sub { $element_code };\n";
        $code .= "        return AST::PackratParser::parse_quantified(\$input_ref, \$pos, \$element_parser, $min, $max);\n";
    }
    
    $code .= "    });\n";
    $code .= "}\n\n";
    
    return $code;
}

=head2 generate_atom_parser($rule_name, $rule_def, $options)

Generate parser for atomic (terminal or simple) rules.

=cut

sub generate_atom_parser {
    my ($rule_name, $rule_def, $options) = @_;
    
    my $value = $rule_def->{value};
    my $return_annotation = $rule_def->{return_annotation};
    
    my $code = "sub parse_$rule_name {\n";
    $code .= "    my (\$input_ref, \$pos) = \@_;\n";
    $code .= "    \$pos = pos(\$\$input_ref) unless defined \$pos;\n";
    $code .= "    \n";
    $code .= "    return AST::PackratParser::memoized_rule('$rule_name', \$input_ref, \$pos, sub {\n";
    $code .= "        my (\$input_ref, \$pos) = \@_;\n";
    
    if (is_terminal($value)) {
        if (is_literal($value)) {
            my $literal = extract_literal_value($value);
            $code .= "        return AST::PackratParser::parse_literal(\$input_ref, \$pos, '$literal');\n";
        } elsif (is_regex($value)) {
            my $pattern = extract_regex_pattern($value);
            $code .= "        return AST::PackratParser::parse_regex(\$input_ref, \$pos, qr/$pattern/);\n";
        } else {
            # Epsilon (empty) production
            $code .= "        return AST::PackratParser::parse_epsilon(\$input_ref, \$pos);\n";
        }
    } else {
        # Rule reference
        my $referenced_rule = extract_rule_name($value);
        $code .= "        return parse_$referenced_rule(\$input_ref, \$pos);\n";
    }
    
    $code .= "    });\n";
    $code .= "}\n\n";
    
    return $code;
}

=head2 generate_element_code($element, $context_rule, $options)

Generate parser code for a single grammar element.

=cut

sub generate_element_code {
    my ($element, $context_rule, $options) = @_;
    
    if (is_terminal($element)) {
        if (is_literal($element)) {
            my $literal = extract_literal_value($element);
            return "AST::PackratParser::parse_literal(\$input_ref, pos(\$\$input_ref), '$literal')";
        } elsif (is_regex($element)) {
            my $pattern = extract_regex_pattern($element);
            return "AST::PackratParser::parse_regex(\$input_ref, pos(\$\$input_ref), qr/$pattern/)";
        }
    } elsif (is_rule_reference($element)) {
        my $rule_name = extract_rule_name($element);
        return "parse_$rule_name(\$input_ref, pos(\$\$input_ref))";
    } elsif (is_quantified($element)) {
        return generate_inline_quantified_code($element, $context_rule, $options);
    }
    
    # Fallback
    return "AST::PackratParser::parse_epsilon(\$input_ref, pos(\$\$input_ref))";
}

=head2 generate_inline_quantified_code($element, $context_rule, $options)

Generate inline code for quantified elements within sequences.

=cut

sub generate_inline_quantified_code {
    my ($element, $context_rule, $options) = @_;
    
    my $inner_element = $element->{element};
    my $quantifier = $element->{quantifier};
    my ($min, $max) = parse_quantifier_bounds($quantifier);
    
    if (is_grouped_quantifier($inner_element)) {
        # CRITICAL: This fixes the SKIPPED grouped quantifier bug!
        my @group_elements = extract_grouped_elements($inner_element);
        
        my $group_code = "do { my \@group_parsers = (";
        for my $group_element (@group_elements) {
            my $element_code = generate_element_code($group_element, $context_rule, $options);
            $group_code .= "sub { $element_code }, ";
        }
        $group_code .= "); AST::PackratParser::parse_grouped_quantified(\$input_ref, pos(\$\$input_ref), \\\@group_parsers, $min, $max) }";
        
        return $group_code;
    } else {
        my $element_code = generate_element_code($inner_element, $context_rule, $options);
        return "AST::PackratParser::parse_quantified(\$input_ref, pos(\$\$input_ref), sub { $element_code }, $min, $max)";
    }
}

=head2 generate_parser_footer()

Generate the parser footer.

=cut

sub generate_parser_footer {
    return <<EOF;

1;

# End of generated packrat parser
EOF
}

