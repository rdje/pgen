=head2 has_alternatives($rule_def)

Check if rule has alternatives (OR groups).

=cut

sub has_alternatives {
    my ($rule_def) = @_;
    return exists $rule_def->{or_groups} && @{$rule_def->{or_groups}} > 1;
}

=head2 has_sequence($rule_def)

Check if rule is a sequence.

=cut

sub has_sequence {
    my ($rule_def) = @_;
    return exists $rule_def->{elements} && ref($rule_def->{elements}) eq 'ARRAY';
}

=head2 has_quantifier($rule_def)

Check if rule has quantifier.

=cut

sub has_quantifier {
    my ($rule_def) = @_;
    return exists $rule_def->{quantifier};
}

=head2 is_grouped_quantifier($element)

Check if element is a grouped quantifier like (',' /\\s*/ expression)*.

=cut

sub is_grouped_quantifier {
    my ($element) = @_;
    
    # Look for grouped structure - this depends on how AST represents groups
    # This may need adjustment based on actual AST structure
    return ref($element) eq 'HASH' && 
           exists $element->{type} && 
           $element->{type} eq 'grouped_sequence';
}

=head2 extract_grouped_elements($grouped_element)

Extract individual elements from a grouped quantifier.

=cut

sub extract_grouped_elements {
    my ($grouped_element) = @_;
    
    # Extract elements from grouped structure
    # This implementation depends on the actual AST representation
    if (ref($grouped_element) eq 'HASH' && exists $grouped_element->{elements}) {
        return @{$grouped_element->{elements}};
    }
    
    return ();
}

=head2 parse_quantifier_bounds($quantifier)

Parse quantifier into min/max bounds.

=cut

sub parse_quantifier_bounds {
    my ($quantifier) = @_;
    
    return (0, 999999) unless defined $quantifier;
    
    if ($quantifier eq '*') {
        return (0, 999999);
    } elsif ($quantifier eq '+') {
        return (1, 999999);
    } elsif ($quantifier eq '?') {
        return (0, 1);
    } elsif ($quantifier =~ /^\{(\d+)\}$/) {
        return ($1, $1);
    } elsif ($quantifier =~ /^\{(\d+),\}$/) {
        return ($1, 999999);
    } elsif ($quantifier =~ /^\{(\d+),(\d+)\}$/) {
        return ($1, $2);
    } elsif ($quantifier =~ /^\{,(\d+)\}$/) {
        return (0, $1);
    }
    
    # Default fallback
    return (1, 1);
}

=head2 is_terminal($element)

Check if element is a terminal.

=cut

sub is_terminal {
    my ($element) = @_;
    
    return 0 unless defined $element;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[0] eq 'quoted_string' || 
               $element->[0] eq 'regex' ||
               $element->[0] eq 'terminal';
    } elsif (ref($element) eq 'HASH') {
        return exists $element->{type} && 
               ($element->{type} eq 'terminal' || 
                $element->{type} eq 'literal' ||
                $element->{type} eq 'regex');
    }
    
    return 0;
}

=head2 is_literal($element)

Check if element is a literal terminal.

=cut

sub is_literal {
    my ($element) = @_;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[0] eq 'quoted_string';
    } elsif (ref($element) eq 'HASH') {
        return exists $element->{type} && $element->{type} eq 'literal';
    }
    
    return 0;
}

=head2 is_regex($element)

Check if element is a regex pattern.

=cut

sub is_regex {
    my ($element) = @_;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[0] eq 'regex';
    } elsif (ref($element) eq 'HASH') {
        return exists $element->{type} && $element->{type} eq 'regex';
    }
    
    return 0;
}

=head2 is_rule_reference($element)

Check if element is a rule reference.

=cut

sub is_rule_reference {
    my ($element) = @_;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[0] eq 'rule_reference';
    } elsif (ref($element) eq 'HASH') {
        return exists $element->{type} && $element->{type} eq 'rule_reference';
    } elsif (!ref($element)) {
        return $element =~ /^[a-zA-Z_]\w*$/;
    }
    
    return 0;
}

=head2 is_quantified($element)

Check if element has quantifier.

=cut

sub is_quantified {
    my ($element) = @_;
    return ref($element) eq 'HASH' && exists $element->{quantifier};
}

=head2 extract_literal_value($element)

Extract literal value from terminal element.

=cut

sub extract_literal_value {
    my ($element) = @_;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[1];
    } elsif (ref($element) eq 'HASH') {
        return $element->{value};
    }
    
    return '';
}

=head2 extract_regex_pattern($element)

Extract regex pattern from regex element.

=cut

sub extract_regex_pattern {
    my ($element) = @_;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[1];
    } elsif (ref($element) eq 'HASH') {
        return $element->{pattern};
    }
    
    return '.*';
}

=head2 extract_rule_name($element)

Extract rule name from rule reference element.

=cut

sub extract_rule_name {
    my ($element) = @_;
    
    if (ref($element) eq 'ARRAY') {
        return $element->[1];
    } elsif (ref($element) eq 'HASH') {
        return $element->{name};
    } elsif (!ref($element)) {
        return $element;
    }
    
    return 'unknown_rule';
}

